    void insertIntoNode(node n,int key,node address){
    int i;
    if( n == null)
	return;
    
    for(i = 0; i < n.size; i++)
	if(n.a[i] == key)
	    return;

    i = n.size-1;
    while(i >= 0 && n.a[i] > key)
    {
	n.a[i+1] = n.a[i];
	n.next[i+1] = n.next[i];
	i--;
    }
    i++;
    n.a[i] = key;
    n.next[i] = address;
    n.size++;
    if( i == n.size-1)
	updateKey(n.parent,n,key);
    }
    
    
     void promote(node n,int key,node address){
    if( n == null)
	return;

    if(n.size < 4)
    {
	insertIntoNode(n,key,address);
	return;
    }

    if( n == root)
    {
	root = new node();
	n.parent = root;
    }
    node newptr = split(n);
    node t;
    if(key < n.a[0])
	  t = newptr;
    else
	  t = n;
    insertIntoNode(t,key,address);
    promote(n.parent,n.a[n.size-1],n);
    promote(newptr.parent,newptr.a[newptr.size-1],newptr);
    
}

    node split(node n){
         int midpoint = (n.size+1)/2;
    int newsize = n.size - midpoint;
    node newptr = new node();
    node child;
    newptr.parent = n.parent;
    int i;
    for(i = 0; i < midpoint; i++)
    {
	newptr.a[i] = n.a[i];
	newptr.next[i] = n.next[i];
	n.a[i] = n.a[i+midpoint];
	n.next[i] = n.next[i+midpoint];
    }
    n.size = midpoint;
    newptr.size = newsize;
    for( i = 0; i < n.size; i++)
    {
	child = n.next[i];
	if(child!= null)
	child.parent = n;
    }
    for( i  = 0; i < newptr.size; i++)
    {
	child = newptr.next[i];
	if(child!= null)
	child.parent = newptr;
    }
    return newptr;
    }
    

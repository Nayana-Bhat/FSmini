     public node fkey1Leaf(int key,int level){
         node ptr = root;
         node prevptr = null;
        level = 0;
    int i;
    while(ptr!=null)
    {
	 i = 0;
	  level++;
	 while(i < ptr.size-1 && key > ptr.a[i])
		i++;
	  prevptr = ptr;
	 ptr = ptr.next[i];
    }
    return prevptr;
    }
     
     
